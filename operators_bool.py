#### BOOLEAN COMPARISON OPERATOR

print("------------BOOLEAN COMPARISON OPERATORS------------")

a = 1
b = 2

print(a == b) #False
print(a != b) #True
print(a > b) #False
print(a < b) #True

#BOOLEAN OPERATORS (AND, NOT, OR)

print("------------BOOLEAN OPERATORS------------")

con1 = True
con2 = False

print(not con1) #False
print(con1 and con2) #False
print(con2 or con1) #True


print(0 or 2)
print('hi' or 'hey')